# Exercicio com a função count()


numeros = [5, 6, 4, 1, 3, 9, 0, 5, 3, 1]


# mostrando dentro da lista o maior numero
print("-------------------------------------")
print(max(numeros))

# mostrando dentro da lista o menor numero
print("-------------------------------------")
print(min(numeros))

# Mostrando o primeiro indice da lista na ocorrencia de um elemento
print("-------------------------------------")
print(numeros.index(1))

# mostra quantas vezes apareceu o numero 1
print(numeros.count(1))

